package Fields;

public class Campaigns {

	int campaignID;
	String campaignDetail;
	int campaignSaleRate;
	

	public int getCampaignID() {
		return campaignID;
	}

	public void setCampaignID(int campaignID) {
		this.campaignID = campaignID;
	}

	public String getCampaignDetail() {
		return campaignDetail;
	}

	public void setCampaignDetail(String campaignDetail) {
		this.campaignDetail = campaignDetail;
	}

	public int getCampaignSaleRate() {
		return campaignSaleRate;
	}

	public void setCampaignSaleRate(int campaignSaleRate) {
		this.campaignSaleRate = campaignSaleRate;
	}
	
	
}
